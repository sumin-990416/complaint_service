/**
 * 행정구역명으로 중심 좌표를 반환. 없으면 null.
 * @param {string} query  예) "대전광역시 중구", "세종특별자치시"
 * @returns {{ lat: number, lng: number } | null}
 */
export function lookupCentroid(query) {
  const key = query.trim()
  const coord = CENTROIDS[key]
  if (coord) return { lat: coord[0], lng: coord[1] }

  // "시/도 시/군/구" 형태로 조합해서 시/도만 매칭 (폴백)
  const parts = key.split(' ')
  if (parts.length >= 2) {
    const sidoOnly = CENTROIDS[parts[0]]
    if (sidoOnly) return { lat: sidoOnly[0], lng: sidoOnly[1] }
  }

  return null
}
